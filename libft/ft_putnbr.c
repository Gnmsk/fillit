#include "libft.h"

void	ft_putnbr(int nb)
{
	if (nb >= 10 || nb <= -10)
	{
		ft_putnbr(nb / 10);
		ft_putchar((nb % 10) * (-1 + 2 * (nb > 0)) + '0');
	}
	else
	{
		if (nb < 0)
			ft_putchar('-');
		ft_putchar((nb % 10) * (-1 + 2 * (nb > 0)) + '0');
	}
}
