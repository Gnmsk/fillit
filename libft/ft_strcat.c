#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int n;
	int c;

	c = 0;
	n = ft_strlen(s1);
	while (s2[c] != '\0')
		s1[n++] = s2[c++];
	s1[n] = '\0';
	return (s1);
}
