#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t i;
	size_t c;

	c = 0;
	i = ft_strlen(s1);
	while (s2[c] != '\0' && c < n)
		s1[i++] = s2[c++];
	s1[i] = '\0';
	return (s1);
}
